def _governance_tables_exist(connection) -> bool:
    return bool(
        connection.execute(
            text(
                """
                SELECT to_regclass('public.import_batches') IS NOT NULL
                   AND to_regclass('public.data_quality_issues') IS NOT NULL
                """
            )
        ).scalar()
    )


def _quote_identifier(value: str) -> str:
    return '"' + value.replace('"', '""') + '"'


def ensure_governance_tables(connection) -> None:
    if _governance_tables_exist(connection):
        return

    connection.execute(
        text(
            """
            CREATE TABLE IF NOT EXISTS import_batches (
                import_batch_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                source_period VARCHAR(32) NOT NULL,
                source_files JSONB NOT NULL DEFAULT '[]'::jsonb,
                files_checksum VARCHAR(128) NOT NULL,
                source_rows INTEGER NOT NULL DEFAULT 0,
                unique_rows INTEGER NOT NULL DEFAULT 0,
                imported_rows INTEGER NOT NULL DEFAULT 0,
                date_reviews INTEGER NOT NULL DEFAULT 0,
                status VARCHAR(24) NOT NULL DEFAULT 'SUCCESS',
                started_at TIMESTAMPTZ NOT NULL DEFAULT now(),
                finished_at TIMESTAMPTZ,
                notes TEXT
            )
            """
        )
    )
    connection.execute(
        text(
            """
            CREATE TABLE IF NOT EXISTS data_quality_issues (
                issue_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                issue_key VARCHAR(180) NOT NULL UNIQUE,
                issue_code VARCHAR(64) NOT NULL,
                severity VARCHAR(16) NOT NULL,
                status VARCHAR(16) NOT NULL DEFAULT 'OPEN',
                employee_id BIGINT REFERENCES employees(employee_id) ON DELETE SET NULL,
                source_period VARCHAR(32) NOT NULL,
                source_reference VARCHAR(255),
                observed_value TEXT,
                recommendation TEXT,
                first_seen_at TIMESTAMPTZ NOT NULL DEFAULT now(),
                last_seen_at TIMESTAMPTZ NOT NULL DEFAULT now(),
                resolved_at TIMESTAMPTZ,
                created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
                updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
                CONSTRAINT ck_data_quality_issues_severity
                    CHECK (severity IN ('BLOCKING', 'REVIEW', 'INFO')),
                CONSTRAINT ck_data_quality_issues_status
                    CHECK (status IN ('OPEN', 'VERIFIED', 'FIXED', 'IGNORED'))
            )
            """
        )
    )
    for ddl in (
        "CREATE INDEX IF NOT EXISTS ix_import_batches_source_period ON import_batches(source_period)",
        "CREATE INDEX IF NOT EXISTS ix_import_batches_started_at ON import_batches(started_at)",
        "CREATE INDEX IF NOT EXISTS ix_data_quality_issues_status ON data_quality_issues(status)",
        "CREATE INDEX IF NOT EXISTS ix_data_quality_issues_severity ON data_quality_issues(severity)",
        "CREATE INDEX IF NOT EXISTS ix_data_quality_issues_employee_id ON data_quality_issues(employee_id)",
        "CREATE INDEX IF NOT EXISTS ix_data_quality_issues_source_period ON data_quality_issues(source_period)",
        "CREATE UNIQUE INDEX IF NOT EXISTS uq_employee_assignments_one_current ON employee_assignments(employee_id) WHERE is_current = true",
        "CREATE UNIQUE INDEX IF NOT EXISTS uq_employee_contracts_one_open ON employee_contracts(employee_id) WHERE end_date IS NULL",
        "CREATE UNIQUE INDEX IF NOT EXISTS uq_employee_identities_nik_number ON employee_identities(identity_number) WHERE upper(identity_type) IN ('NIK', 'KTP')",
    ):
        connection.execute(text(ddl))


def ensure_governance_tables_with_admin() -> None:
    admin_user = os.getenv("DB_ADMIN_USER", "").strip()
    admin_password = os.getenv("DB_ADMIN_PASSWORD", "").strip()
    app_user = os.getenv("DB_USER", "").strip()
    db_name = os.getenv("DB_NAME", "").strip()
    db_host = os.getenv("DB_HOST", "localhost").strip()
    db_port = int(os.getenv("DB_PORT", "5432").strip() or "5432")

    if not admin_user or not admin_password or not db_name:
        return

    admin_url = URL.create(
        drivername="postgresql+psycopg2",
        username=admin_user,
        password=admin_password,
        host=db_host,
        port=db_port,
        database=db_name,
    )
    admin_engine = create_engine(admin_url, pool_pre_ping=True, future=True)
    try:
        with admin_engine.begin() as connection:
            if not _governance_tables_exist(connection):
                connection.execute(
                    text(
                        """
                        CREATE TABLE IF NOT EXISTS import_batches (
                            import_batch_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                            source_period VARCHAR(32) NOT NULL,
                            source_files JSONB NOT NULL DEFAULT '[]'::jsonb,
                            files_checksum VARCHAR(128) NOT NULL,
                            source_rows INTEGER NOT NULL DEFAULT 0,
                            unique_rows INTEGER NOT NULL DEFAULT 0,
                            imported_rows INTEGER NOT NULL DEFAULT 0,
                            date_reviews INTEGER NOT NULL DEFAULT 0,
                            status VARCHAR(24) NOT NULL DEFAULT 'SUCCESS',
                            started_at TIMESTAMPTZ NOT NULL DEFAULT now(),
                            finished_at TIMESTAMPTZ,
                            notes TEXT
                        )
                        """
                    )
                )
                connection.execute(
                    text(
                        """
                        CREATE TABLE IF NOT EXISTS data_quality_issues (
                            issue_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                            issue_key VARCHAR(180) NOT NULL UNIQUE,
                            issue_code VARCHAR(64) NOT NULL,
                            severity VARCHAR(16) NOT NULL,
                            status VARCHAR(16) NOT NULL DEFAULT 'OPEN',
                            employee_id BIGINT REFERENCES employees(employee_id) ON DELETE SET NULL,
                            source_period VARCHAR(32) NOT NULL,
                            source_reference VARCHAR(255),
                            observed_value TEXT,
                            recommendation TEXT,
                            first_seen_at TIMESTAMPTZ NOT NULL DEFAULT now(),
                            last_seen_at TIMESTAMPTZ NOT NULL DEFAULT now(),
                            resolved_at TIMESTAMPTZ,
                            created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
                            updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
                            CONSTRAINT ck_data_quality_issues_severity
                                CHECK (severity IN ('BLOCKING', 'REVIEW', 'INFO')),
                            CONSTRAINT ck_data_quality_issues_status
                                CHECK (status IN ('OPEN', 'VERIFIED', 'FIXED', 'IGNORED'))
                        )
                        """
                    )
                )
            for ddl in (
                "CREATE INDEX IF NOT EXISTS ix_import_batches_source_period ON import_batches(source_period)",
                "CREATE INDEX IF NOT EXISTS ix_import_batches_started_at ON import_batches(started_at)",
                "CREATE INDEX IF NOT EXISTS ix_data_quality_issues_status ON data_quality_issues(status)",
                "CREATE INDEX IF NOT EXISTS ix_data_quality_issues_severity ON data_quality_issues(severity)",
                "CREATE INDEX IF NOT EXISTS ix_data_quality_issues_employee_id ON data_quality_issues(employee_id)",
                "CREATE INDEX IF NOT EXISTS ix_data_quality_issues_source_period ON data_quality_issues(source_period)",
                "CREATE UNIQUE INDEX IF NOT EXISTS uq_employee_assignments_one_current ON employee_assignments(employee_id) WHERE is_current = true",
                "CREATE UNIQUE INDEX IF NOT EXISTS uq_employee_contracts_one_open ON employee_contracts(employee_id) WHERE end_date IS NULL",
                "CREATE UNIQUE INDEX IF NOT EXISTS uq_employee_identities_nik_number ON employee_identities(identity_number) WHERE upper(identity_type) IN ('NIK', 'KTP')",
            ):
                connection.execute(text(ddl))
            if app_user:
                quoted_user = _quote_identifier(app_user)
                connection.execute(
                    text(
                        f"GRANT SELECT, INSERT, UPDATE, DELETE ON import_batches, data_quality_issues TO {quoted_user}"
                    )
                )
                connection.execute(
                    text(
                        f"GRANT USAGE, SELECT, UPDATE ON ALL SEQUENCES IN SCHEMA public TO {quoted_user}"
                    )
                )
    finally:
        admin_engine.dispose()


def calculate_files_checksum(paths: Iterable[Path]) -> str:
    digest = hashlib.sha256()
    for path in sorted(paths, key=lambda item: item.name.lower()):
        digest.update(path.name.encode("utf-8"))
        digest.update(str(path.stat().st_size).encode("ascii"))
        with path.open("rb") as file_obj:
            for chunk in iter(lambda: file_obj.read(1024 * 1024), b""):
                digest.update(chunk)
    return digest.hexdigest()


def record_import_batch(
    connection,
    *,
    source_files: Iterable[Path],
    source_rows: int,
    unique_rows: int,
    imported_rows: int,
    date_reviews: int,
    status: str = "SUCCESS",
    notes: str | None = None,
    source_period: str = SOURCE_PERIOD,
) -> int:
    ensure_governance_tables(connection)
    file_paths = list(source_files)
    payload = [
        {
            "name": path.name,
            "path": str(path),
            "size": path.stat().st_size,
        }
        for path in file_paths
    ]
    return int(
        connection.execute(
            text(
                """
                INSERT INTO import_batches (
                    source_period, source_files, files_checksum, source_rows,
                    unique_rows, imported_rows, date_reviews, status, finished_at, notes
                )
                VALUES (
                    :source_period, CAST(:source_files AS jsonb), :files_checksum,
                    :source_rows, :unique_rows, :imported_rows, :date_reviews,
                    :status, now(), :notes
                )
                RETURNING import_batch_id
                """
            ),
            {
                "source_period": source_period,
                "source_files": json.dumps(payload),
                "files_checksum": calculate_files_checksum(file_paths),
                "source_rows": source_rows,
                "unique_rows": unique_rows,
                "imported_rows": imported_rows,
                "date_reviews": date_reviews,
                "status": status,
                "notes": notes,
            },
        ).scalar_one()
    )


