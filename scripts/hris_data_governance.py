def _issue_key(issue_code: str, employee_id: int | None, observed_value: str | None) -> str:
    raw_value = f"{issue_code}|{employee_id or 'none'}|{observed_value or ''}"
    suffix = hashlib.sha1(raw_value.encode("utf-8")).hexdigest()[:16]
    return f"{issue_code}:{employee_id or 'none'}:{suffix}"


def build_current_issue_rows(connection, *, source_period: str = SOURCE_PERIOD) -> list[dict[str, object]]:
    rows: list[dict[str, object]] = []
    for issue_code, severity, description, recommendation, sql in ISSUE_QUERIES:
        for row in connection.execute(text(sql)).mappings():
            employee_id = row.get("employee_id")
            observed_value = row.get("observed_value")
            rows.append(
                {
                    "issue_key": _issue_key(
                        issue_code,
                        int(employee_id) if employee_id is not None else None,
                        str(observed_value) if observed_value is not None else None,
                    ),
                    "issue_code": issue_code,
                    "severity": severity,
                    "employee_id": employee_id,
                    "source_period": source_period,
                    "source_reference": row.get("source_reference"),
                    "observed_value": observed_value or description,
                    "recommendation": recommendation,
                }
            )
    return rows


def sync_data_quality_issues(connection, *, source_period: str = SOURCE_PERIOD) -> dict[str, int]:
    ensure_governance_tables(connection)
    rows = build_current_issue_rows(connection, source_period=source_period)
    current_keys = [str(row["issue_key"]) for row in rows]
    upserted = 0

    for row in rows:
        connection.execute(
            text(
                """
                INSERT INTO data_quality_issues (
                    issue_key, issue_code, severity, status, employee_id,
                    source_period, source_reference, observed_value, recommendation,
                    first_seen_at, last_seen_at, resolved_at
                )
                VALUES (
                    :issue_key, :issue_code, :severity, 'OPEN', :employee_id,
                    :source_period, :source_reference, :observed_value, :recommendation,
                    now(), now(), NULL
                )
                ON CONFLICT (issue_key) DO UPDATE
                SET issue_code = EXCLUDED.issue_code,
                    severity = EXCLUDED.severity,
                    employee_id = EXCLUDED.employee_id,
                    source_period = EXCLUDED.source_period,
                    source_reference = EXCLUDED.source_reference,
                    observed_value = EXCLUDED.observed_value,
                    recommendation = EXCLUDED.recommendation,
                    status = CASE
                        WHEN data_quality_issues.status = 'FIXED' THEN 'OPEN'
                        ELSE data_quality_issues.status
                    END,
                    last_seen_at = now(),
                    resolved_at = CASE
                        WHEN data_quality_issues.status = 'FIXED' THEN NULL
                        ELSE data_quality_issues.resolved_at
                    END,
                    updated_at = now()
                """
            ),
            row,
        )
        upserted += 1

    if current_keys:
        fixed = connection.execute(
            text(
                """
                UPDATE data_quality_issues
                SET status = 'FIXED',
                    resolved_at = now(),
                    updated_at = now()
                WHERE source_period = :source_period
                  AND status IN ('OPEN', 'VERIFIED')
                  AND issue_key NOT IN :issue_keys
                """
            ).bindparams(bindparam("issue_keys", expanding=True)),
            {"source_period": source_period, "issue_keys": current_keys},
        ).rowcount
    else:
        fixed = connection.execute(
            text(
                """
                UPDATE data_quality_issues
                SET status = 'FIXED',
                    resolved_at = now(),
                    updated_at = now()
                WHERE source_period = :source_period
                  AND status IN ('OPEN', 'VERIFIED')
                """
            ),
            {"source_period": source_period},
        ).rowcount

    counts = get_issue_counts(connection, source_period=source_period)
    return {
        "upserted": upserted,
        "fixed": int(fixed or 0),
        **counts,
    }


def get_issue_counts(connection, *, source_period: str = SOURCE_PERIOD) -> dict[str, int]:
    ensure_governance_tables(connection)
    rows = connection.execute(
        text(
            """
            SELECT severity, COUNT(*) AS total
            FROM data_quality_issues
            WHERE source_period = :source_period
              AND status = 'OPEN'
            GROUP BY severity
            """
        ),
        {"source_period": source_period},
    ).all()
    counts = {"open_blocking": 0, "open_review": 0, "open_info": 0}
    for severity, total in rows:
        key = f"open_{str(severity).lower()}"
        if key in counts:
            counts[key] = int(total or 0)
    counts["open_total"] = counts["open_blocking"] + counts["open_review"] + counts["open_info"]
    counts["watch_total"] = counts["open_blocking"] + counts["open_review"]
    return counts


def export_review_csv(connection, output_path: Path = DEFAULT_REPORT_PATH, *, source_period: str = SOURCE_PERIOD) -> int:
    ensure_governance_tables(connection)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    rows = connection.execute(
        text(
            """
            SELECT
                i.issue_code,
                i.severity,
                i.status,
                e.employee_id,
                e.employee_no,
                e.full_name,
                COALESCE(d.division_name, '-') AS current_division,
                COALESCE(p.position_name, '-') AS current_position,
                COALESCE(i.observed_value, '-') AS observed_value,
                COALESCE(i.recommendation, '-') AS recommendation
            FROM data_quality_issues i
            LEFT JOIN employees e ON e.employee_id = i.employee_id
            LEFT JOIN employee_assignments a ON a.employee_id = e.employee_id AND a.is_current = true
            LEFT JOIN divisions d ON d.division_id = a.division_id
            LEFT JOIN positions p ON p.position_id = a.position_id
            WHERE i.source_period = :source_period
              AND i.status IN ('OPEN', 'VERIFIED', 'IGNORED')
            ORDER BY
                CASE i.severity
                    WHEN 'BLOCKING' THEN 1
                    WHEN 'REVIEW' THEN 2
                    ELSE 3
                END,
                i.issue_code,
                d.division_name,
                e.employee_no
            """
        ),
        {"source_period": source_period},
    ).mappings().all()

    fieldnames = [
        "issue_code",
        "severity",
        "status",
        "employee_id",
        "employee_no",
        "full_name",
        "current_division",
        "current_position",
        "observed_value",
        "recommendation",
    ]
    with output_path.open("w", newline="", encoding="utf-8-sig") as csv_file:
        writer = csv.DictWriter(csv_file, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(dict(row) for row in rows)
    return len(rows)
